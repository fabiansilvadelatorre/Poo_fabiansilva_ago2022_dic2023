public class process {
    /**
     * realisa operaciones aridmeticas y modulo dependiendo las variables
     * @param num1 var 1
     * @param num2 var2
     * @return var 3 = resultado
     */
    /**
     *realisa ina suma de 2 variables
     */

    public static int suma (int num1,int num2){
        return num1+num2;}
    /**
     *realisa ina resta de 2 variables
     */
        public static int resta (int num1,int num2){
            return num1-num2;}
    /**
     *realisa ina multiplicacion de 2 variables
     */
            public static int multiplicacion (int num1,int num2){
                return num1*num2;}
    /**
     *realisa ina divicion de 2 variables
     */
                public static int divicion (int num1,int num2){
                    return num1/num2;}
    /**
     *realisa ina modulo de 2 variables
     */
    public static int modulo (int num1,int num2){
        return num1%num2;}

}

